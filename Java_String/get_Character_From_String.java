package Java_String;

public class get_Character_From_String {
	public static void main(String[] args) {
		String str1 = "Hello";
		System.out.println(str1.charAt(2));// op:-l

		// toLowercase
		System.out.println(str1.toLowerCase());// hello
		// toUpperCase
		System.out.println(str1.toUpperCase());// Hello

	}

}
